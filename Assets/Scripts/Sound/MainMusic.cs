using UnityEngine;

namespace Sound
{
    public class MainMusic : MonoBehaviour
    {
        public static MainMusic Instance;

        private void Awake()
        {
            if (Instance == null)
            {
                Instance = this;
                DontDestroyOnLoad(gameObject);
            }
            else
            {
                Destroy(gameObject);
            }
        }
    }
}